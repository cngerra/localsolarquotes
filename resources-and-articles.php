					<h6>What is Rent to Own Housing?</h6>
					Also called leasing to own, the process of rent to own housing works similar to that of a car lease. Renters agree to pay a set amount to the owner for each month they live in the house. At the end of a set period (average is three years) the renters have the option of buying the home. The rent payments are like income for the seller, and a portion of the payments goes towards a down payment for the buyers.<br><br>
					<ins class="adslot_200x90 adslot_pull_center" style="background-color: #dbfdf9;"></ins>
					<h6>How it Works</h6>
					There are certain steps one must take when purchasing a home via rent to own.<br><br>
					<strong>1) Agreed Purchase Price</strong>: When a buyer moves into a home purchased via the lease option, the buyer and the seller MUST agree on the sale price of the home.<br><br>
					<strong>2) Other Buyers Locked Out</strong>: The buyer in a rent to own housing situation typically pays the seller a fee. This fee prevents the seller from selling the home or from offering to rent the home to another potential buyer.<br><br>
					<strong>3) Occupancy</strong>: Once the above situations are taken care of, the buyer then occupies the home as a renter. The period of time is agreed upon between buyer and seller and can last anywhere from one to three years.<br><br>
					<strong>4) Credit builds towards purchase</strong>: A portion of rent payments is applied to the purchase price of the home. An example of this would be:  rent is $1500 a month, $750 of each payment is designated as a rent to own option credit. Over 24 months the buyer will have accumulated an $18,000 down payment.<br><br>
